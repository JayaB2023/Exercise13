# LISTS
# [] = list

# we had to add square brackets around 'Oke' to add it to the list
cheese = ['Cheddar', 'Stilton', 'Cornish Yarg']
cheese += ['Oke']
print(cheese)

# extend function adds Brie and Feta to the end of the list
cheese = ['Cheddar', 'Stilton', 'Cornish Yarg']
cheese.extend(['Brie', 'Feta'])
print(cheese)
